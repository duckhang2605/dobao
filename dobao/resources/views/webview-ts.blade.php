	<h1>Nhạc sĩ Đỗ Bảo</h1>
	<p>
		Đỗ Bảo (tên đầy đủ là Đỗ Quốc Bảo, sinh ngày 30 tháng 6 năm 1978 tại Hà Nội), là nhạc sĩ, nhà sản xuất âm nhạc. Anh là một trong những tên tuổi lớn của làng nhạc nhẹ Việt Nam với nhiều sản phẩm thành công, uy tín, hợp tác với nhiều nghệ sĩ trong và ngoài nước. 
		<br>
		<br>
		Sinh ra trong một gia đình không có ai làm nghệ thuật, song năng khiếu của Đỗ Bảo lại được bộc lộ khá sớm. Năm 1993 khi còn trên ghế nhà trường, Đỗ Bảo lập nên ban nhạc Sao Mai, tham gia nhiều chương trình trình diễn lớn nhỏ khắp nơi trên cả nước. Kể từ năm 1994, ban nhạc có được tiếng tăm và được mời đi trình diễn tại nhiều địa điểm lớn ở Hà Nội.
		<br>
		<br>
		Cũng từ đây, Đỗ Bảo bắt đầu sự nghiệp sáng tác qua việc tiếp xúc với nhiều thể loại âm nhạc từ khắp nơi trên thế giới. Ngay khi vẫn còn là sinh viên của Nhạc viện Hà Nội, anh đã trở nên nổi tiếng khi thực hiện phần lớn phần hòa âm phối khí cho album Nhật thực (2002) của ca sĩ Trần Thu Hà và nhạc sĩ Ngọc Đại, tạo được tiếng vang rất lớn trong cộng đồng người nghe nhạc.
		<br>
		<br>
		Đỗ Bảo tiếp tục có được thành công lớn khi hoàn thiện bộ album sự nghiệp là Cánh cung (2004), Thời gian để yêu (2008) và Chuyện của mặt trời – Chuyện của chúng ta (2013). Bộ 3 album này giúp anh có được nhiều đề cử tại Giải thưởng Âm nhạc Cống hiến, nhưng hơn hết giúp anh 2 lần giành cú đúp "Nhạc sĩ của năm" và "Album của năm" (2009 và 2014). 
		<br>
		<br>
		Trong khoảng thời gian trên, Đỗ Bảo cũng tham gia cộng tác với nhiều nghệ sĩ trong và ngoài nước như ca sĩ Tùng Dương, Nguyên Thảo, Tấn Minh, Nguyễn Ngọc Anh hay nghệ sĩ đàn tranh Võ Vân Ánh. Đáng kể nhất là album Những ô màu khối lập phương (2007) của ca sĩ Tùng Dương do anh phối khí và biên tập cũng được trao giải "Album của năm" tại Giải thưởng âm nhạc Cống hiến năm 2008. 
		<br><br>

		Năm 2006, anh thành lập nên công ty Bảo Lý Art cùng nghệ sĩ Việt kiều Nhất Lý, thực hiện thành công chuỗi chương trình “Gió bình minh”, sau đó được VTV bình chọn là "Sự kiện văn hóa tiêu biểu của năm". 
		<br><br>
		Năm 2010, Đỗ Bảo trực tiếp khởi xướng và dàn dựng âm nhạc cho đại nhạc hội “Là người con đất Việt” ủng hộ đồng bào miền Trung. Ngoài ra, anh còn tham gia hòa âm phối khí cho rất nhiều chương trình truyền hình cũng như các buổi trình diễn nghệ thuật lớn nhỏ khác. Thành công lớn cùng phong cách riêng biệt trong sáng tác, ca từ và hòa âm giúp anh được công chúng yêu nhạc đặt biệt danh "Bảo pop".
		<br><br>
		Ngày 8 tháng 12 năm 2013, anh tổ chức thành công liveshow tác giả mang tên Cánh cung – Đỗ Bảo – Live in Hanoi tại Cung Văn hóa Hữu nghị Hà Nội nhằm kỷ niệm sự nghiệp 20 năm sáng tác của cá nhân mình, quy tụ nhiều nghệ sĩ lớn từ khắp nơi trên cả nước. 
		<br><br>
		Kể từ năm 2003, anh trở thành giảng viên của trường Cao đẳng Văn hóa Nghệ thuật Quân đội, nay là Đại học Văn hóa Nghệ thuật Quân đội. Hiện nay, Đỗ Bảo đang là tổ trưởng bộ môn sáng tác chỉ huy khoa Âm nhạc thuộc Đại học Văn hóa Nghệ thuật Quân đội.
	</p>
